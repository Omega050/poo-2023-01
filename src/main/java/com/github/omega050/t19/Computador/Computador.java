package com.github.omega050.t19.Computador;

public class Computador {
    private String marca;
    private String modelo;
    
    public Computador(String marca, String modelo) {
        this.marca = marca;
        this.modelo = modelo;
    }
    
    public String getMarca() {
            return marca;
    }
    
    public String getModelo() {
        return modelo;
    }
}
    

