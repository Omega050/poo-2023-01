package com.github.omega050.t19.Pixel;

public class Ponto {
    private int posX;
    private int posY;

    public Ponto(int x, int y){
        this.posX=x;
        this.posY=y;
    }

    public int getPosX() {
        return posX;
    }
    
    public int getPosY() {
        return posY;
    }
}
