package com.github.omega050.t21;

public class Bebida {
        private String nome;
        private int val;

    public Bebida(String nome, int val) {
        this.nome = nome;
        this.val = val;
    }

    public int getVal() {
        return this.val;
    }

    public void setVal(int val) {
        this.val = val;
    }

    public String getNome() {
        return this.nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }     
}
    

